let i;

for (i = 1; i <= 5; i++) {
  console.log(i);
}

console.log("Counting completed!");

for (i = 5; i >= 1; i--) {
  console.log(i);
}

console.log("Countdown finished!");

i = 1;
while (i <= 5) {
  console.log(i);
  i++;
}
console.log("Counting completed!");

i = 5;
while (i >= 1) {
  console.log(i);
  i--;
}
console.log("Countdown finished!");

let year = 2018;
while (year <= 2022) {
  console.log(year++);
}
